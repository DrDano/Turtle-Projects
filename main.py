
from turtle import Turtle, Screen

big_john_turtle = Turtle()
big_john_turtle.shape("turtle")
big_john_turtle.color("AntiqueWhite")
big_john_turtle.pencolor("black")

for _ in range(0, 4):
    big_john_turtle.forward(100)
    big_john_turtle.right(90)










screen = Screen()
screen.exitonclick()

